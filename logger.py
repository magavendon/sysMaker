# Redefined functions
NOW = datetime.now

# Created functions
def log(message, logDate = False):
    f = open(LOGFILE, 'a')
    if logDate:
        f.write('{}\n'.format(NOW()))

    f.write('{}\n'.format(message))
    f.write('\n')
    f.close()
