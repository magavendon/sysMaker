# Main Menu
def mainMenu():
    # Main menu options
    choices = ['Random System',
               'Optional System',
               'Quit']
    selection = useMenu(choices)

    # Perform correct action
    if selection == 1:
        # Create a completely random system
        createSystem()
    elif selection == 2:
        # Give the user options for creating a system
        createSystem(False)
    elif selection == 3:
        # Quit the program
        quit()

# Create system
